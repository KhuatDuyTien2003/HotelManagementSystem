    private void btnXoaPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPhongActionPerformed
        // TODO add your handling code here:
        if (btnXoaPhong.getText().equalsIgnoreCase("Không Lưu")) {
            btnSuaPhong.setEnabled(true);
            btnThemPhong.setEnabled(true);
            btnThemPhong.setText("Thêm");
            btnXoaPhong.setText("Xóa");
            btnSuaPhong.setText("Sửa");
            txtDVTinh.setEnabled(false);
            txtDonGia.setEnabled(false);
            txtMaPhong.setEnabled(false);
            cbLoaiPhong.setEnabled(false);
            cbTinhTrang.setEnabled(false);

        } else {
            if (txtDVTinh.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Chọn dữ liệu ở dưới trước!!!!");
            } else {
                int rel = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa", "Xác Nhận Xóa", JOptionPane.YES_NO_OPTION);
                if (rel == JOptionPane.YES_OPTION) {
                    ChucNang.xoaPhong(txtMaPhong.getText());
                    DefaultTableModel tbl = (DefaultTableModel) tbl_Phong.getModel();
                    ChucNang.hienThiPhong(tbl);
                }
            }
        }
    }//GEN-LAST:event_btnXoaPhongActionPerformed

