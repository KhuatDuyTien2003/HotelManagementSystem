    private void tbl_PhongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_PhongMouseClicked
        // TODO add your handling code here:
        int rowSelect = tbl_Phong.getSelectedRow();
        if (rowSelect > -1) {
            txtMaPhong.setText(tbl_Phong.getValueAt(rowSelect, 0).toString());
            txtDonGia.setText(tbl_Phong.getValueAt(rowSelect, 3).toString());
            txtDVTinh.setText(tbl_Phong.getValueAt(rowSelect, 4).toString());
            cbLoaiPhong.setSelectedItem(tbl_Phong.getValueAt(rowSelect, 1));

            cbTinhTrang.setSelectedItem(tbl_Phong.getValueAt(rowSelect, 2));

        }
    }//GEN-LAST:event_tbl_PhongMouseClicked

