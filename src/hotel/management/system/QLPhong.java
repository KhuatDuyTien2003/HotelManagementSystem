    private void btnThemPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemPhongActionPerformed
        // TODO add your handling code here:
        if (btnThemPhong.getText() == "Lưu") {
            DBConnection c = new DBConnection();
            String maPhong = txtMaPhong.getText();
            String loaiPhong = cbLoaiPhong.getSelectedItem().toString();
            String tinhTrang = (String) cbTinhTrang.getSelectedItem();
            String donGia = txtDonGia.getText();
            String dvTinh = txtDVTinh.getText();

            ChucNang.suaPhong(maPhong, loaiPhong, tinhTrang, donGia, dvTinh);
            DefaultTableModel tbl = (DefaultTableModel) tbl_Phong.getModel();
            ChucNang.hienThiPhong(tbl);
            btnSuaPhong.setEnabled(true);
            btnXoaPhong.setText("Xóa");
            btnThemPhong.setText("Thêm");
            txtDVTinh.setEnabled(false);
            txtDonGia.setEnabled(false);
            txtMaPhong.setEnabled(false);
            cbLoaiPhong.setEnabled(false);
            cbTinhTrang.setEnabled(false);
            txtDVTinh.setText("");
            txtDonGia.setText("");
            txtMaPhong.setText("");

        } else {
            btnThemPhong.setEnabled(false);
            btnSuaPhong.setText("Lưu");
            btnXoaPhong.setText("Không Lưu");
            txtDVTinh.setEnabled(true);
            txtDonGia.setEnabled(true);
            txtMaPhong.setEnabled(true);
            cbLoaiPhong.setEnabled(true);
            cbTinhTrang.setEnabled(true);

        }


    }//GEN-LAST:event_btnThemPhongActionPerformed

    private void btnSuaPhongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaPhongActionPerformed
        // TODO add your handling code here:
        if (txtDVTinh.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(rootPane, "Chọn dữ liệu ở dưới trước!!!!");
        } else {
            if (btnSuaPhong.getText() == "Lưu") {
                DBConnection c = new DBConnection();
                String maPhong = txtMaPhong.getText();
                String loaiPhong = cbLoaiPhong.getSelectedItem().toString();
                String tinhTrang = cbTinhTrang.getSelectedItem().toString();
                String donGia = txtDonGia.getText();
                String dvTinh = txtDVTinh.getText();

                ChucNang.themPhong(maPhong, loaiPhong, tinhTrang, donGia, dvTinh);
                DefaultTableModel tbl = (DefaultTableModel) tbl_Phong.getModel();
                ChucNang.hienThiPhong(tbl);
                btnThemPhong.setEnabled(true);
                btnXoaPhong.setText("Xóa");
                btnSuaPhong.setText("Sửa");
                txtDVTinh.setEnabled(false);
                txtDonGia.setEnabled(false);
                txtMaPhong.setEnabled(false);
                cbLoaiPhong.setEnabled(false);
                cbTinhTrang.setEnabled(false);
                txtDVTinh.setText("");
                txtDonGia.setText("");
                txtMaPhong.setText("");

            } else {
                btnSuaPhong.setEnabled(false);
                btnThemPhong.setText("Lưu");
                btnXoaPhong.setText("Không Lưu");
                txtDVTinh.setEnabled(true);
                txtDonGia.setEnabled(true);
                txtMaPhong.setEnabled(true);
                cbLoaiPhong.setEnabled(true);
                cbTinhTrang.setEnabled(true);

            }
        }

    }//GEN-LAST:event_btnSuaPhongActionPerformed


